export const iconRenderer = function (project) {
  if (project.lat && project.lng) {
    switch (project.kind) {
      case 'urban planning':
        return redIcon;
      case 'urban designing':
        return greenIcon;
      case 'regional planning':
        return violetIcon;
      case 'urban regeneration':
        return greyIcon;
      case 'historical quarters conservation':
        return goldIcon;
      case 'traffic':
        return blackIcon;
      case 'architecture':
        return yellowIcon;
      default:
        return orangeIcon;
    }
  }
}
